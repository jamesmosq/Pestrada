        <th>Operador</th>
        <th>Nombre</th>
        <th>Ejemplo</th>
        <th>Resultado</th>
    </tr>
    <tr>
        <td>==</td>
        <td>Igual</td>
        <td>$x == $y</td>
        <td>Devuelve true si $x es igual a $y</td>
    </tr>
    <tr>
        <td>===</td>
        <td>Idéntico</td>
        <td>$x === $y</td>
        <td>Devuelve true si $x es igual a $y y son del mismo tipo de dato</td>
    </tr>
    <tr>
        <td>!=</td>
        <td>No igual</td>
        <td>$x != $y</td>
        <td>Devuelve true si $x no es igual a $y</td>
    </tr>
    <tr>
        <td>&lt;&gt;</td>
        <td>No igual</td>
        <td>$x &lt;&gt; $y</td>
        <td>Devuelve true si $x no es igual a $y</td>
    </tr>
    <tr>
        <td>!==</td>
        <td>No idéntico</td>
        <td>$x !== $y</td>
        <td>Devuelve true si $x no es igual a $y, o si no son del mismo tipo de dato</td>
    </tr>
    <tr>
        <td>&gt;</td>
        <td>Mayor que</td>
        <td>$x &gt; $y</td>
        <td>Devuelve true si $x es mayor que $y</td>
    </tr>
    <tr>
        <td>&lt;</td>
        <td>Menor que</td>
        <td>$x &lt; $y</td>
        <td>Devuelve true si $x es menor que $y</td>
    </tr>
    <tr>
        <td>&gt;=</td>
        <td>Mayor o igual que</td>
        <td>$x &gt;= $y</td>
        <td>Devuelve true si $x es mayor o igual que $y</td>
    </tr>
    <tr>
        <td>&lt;=</td>
        <td>Menor o igual que</td>
        <td>$x &lt;= $y</td>
        <td>Devuelve true si $x es menor o igual que $y</td>
    </tr>
    <tr>
        <td>&lt;=&gt;</td>
        <td>Operador de Nave espacial</td>
        <td>$x &lt;=&gt; $y</td>
        <td>Devuelve un entero menor que, igual a o mayor que cero, dependiendo si $x es menor que, igual a, o mayor que $y. Introducido en PHP 7.</td>
    </tr>
