<h1>Practica condicionales</h1>
<h2>1. Ejercicios con if:</h2>
<h3>Verificación de Edad:</h3>
<p>Pide al usuario que ingrese su edad y muestra un mensaje que diga "Eres mayor de edad" si la edad ingresada es igual o mayor a 18, de lo contrario, muestra un mensaje que diga "Eres menor de edad".</p>

<h3>Verificación de Número Positivo o Negativo:</h3>
<p>Pide al usuario que ingrese un número y muestra un mensaje que diga "El número es positivo" si es mayor que cero, "El número es negativo" si es menor que cero, y "El número es cero" si es igual a cero.</p>

<h3>Día de la Semana:</h3>
<p>Crea una variable $día que almacene el día de la semana en texto (por ejemplo, "Lunes"). Utiliza una estructura condicional para imprimir un mensaje personalizado dependiendo del día. Por ejemplo, "¡Feliz Lunes!".</p>

<h2> 2. Ejercicios con else:</h2>
<h3>Verificación de Contraseña:</h3>
<p>Pide al usuario que ingrese una contraseña. Si la contraseña ingresada es igual a "secreto", muestra un mensaje que diga "Contraseña correcta", de lo contrario, muestra un mensaje que diga "Contraseña incorrecta".</p>
<h3>Cálculo de Bono:</h3>
<p>Pide al usuario que ingrese su salario mensual. Si el salario es mayor o igual a $1000, muestra un mensaje que diga "Tienes derecho a un bono", de lo contrario, muestra un mensaje que diga "No cumples con el salario mínimo para el bono".</p>
<h3>Clasificación de Edades:</h3>
<p>Pide al usuario que ingrese su edad. Si la edad está entre 0 y 12 años, muestra un mensaje que diga "Eres un niño", si está entre 13 y 19 años, muestra un mensaje que diga "Eres un adolescente", y si es mayor de 19 años, muestra un mensaje que diga "Eres un adulto".</p>

<h2>3. Ejercicios con elseif:</h2>
<h3>Categorización de Productos:</h3>
<p>Pide al usuario que ingrese el nombre de un producto. Si el producto es "Libro", muestra un mensaje que diga "El producto es un libro", si es "Ropa", muestra un mensaje que diga "El producto es ropa", y si es "Electrodoméstico", muestra un mensaje que diga "El producto es un electrodoméstico".</p>
<h3>Verificación de Año Bisiesto:</h3>
<p>Pide al usuario que ingrese un año. Utiliza una estructura condicional para determinar si el año ingresado es bisiesto o no, y muestra un mensaje correspondiente.</p>
<h3>Clasificación de Números:</h3>
<p>Pide al usuario que ingrese un número. Si el número es positivo, muestra un mensaje que diga "El número es positivo", si es negativo, muestra un mensaje que diga "El número es negativo", y si es cero, muestra un mensaje que diga "El número es cero".</p>

<h2>4. Ejercicios con switch:</h2>
<h3>Día de la Semana:</h3>
<p>Pide al usuario que ingrese el número de un día de la semana (1 para Lunes, 2 para Martes, etc.). Utiliza una estructura switch para mostrar el nombre del día correspondiente.</p>
<h3>Categorización de Letras:</h3>
<p>Pide al usuario que ingrese una letra del alfabeto. Utiliza una estructura switch para determinar si la letra ingresada es una vocal o una consonante, y muestra un mensaje correspondiente.</p>
<h3>Días del Mes:</h3>
<p>Pide al usuario que ingrese el número de un mes (1 para Enero, 2 para Febrero, etc.). Utiliza una estructura switch para mostrar el número de días correspondientes a ese mes.</p>
