<?php
// Definición de la función suma
function suma($num1, $num2) {
    $resultado = $num1 + $num2;
    return $resultado;
}

// Llamada a la función con dos números
$numero1 = 10;
$numero2 = 5;
$total = suma($numero1, $numero2);

// Imprimir el resultado
echo "La suma de $numero1 y $numero2 es: $total";
?>
